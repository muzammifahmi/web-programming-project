    <h1 align="center"><font face="times new roman">
        PEMANDANGAN PANTAI
    </font></h1>
    <hr color="black">
    <p align="justify"><font face="calibri">
    Di suatu sore yang cerah, angin laut membawa aroma segar yang menggoda,dan ombak dengan lembut menghantam pasir putih di sepanjang pantai. 
    Di sanalah kamu berdiri, menikmati setiap hembusan angin yang menyentuh wajahmu, seolah-olah alam mengajaknya berbicara.Kamu memutuskan untuk berjalan di sepanjang bibir pantai, 
    merasakan hangatnya pasir di antara jemarimu. Di kejauhan, terlihat sekumpulan burung camar terbang rendah di atas permukaan laut, 
    sesekali menyelam untuk menangkap ikan. Suara alam begitu harmonis, berpadu dengan deru ombak yang terus-menerus menghantam pantai.
    Setelah beberapa saat berjalan, kamu menemukan sebuah tempat yang agak sepi, di bawah pohon kelapa yang rindang. Kamu duduk di sana, memandangi luasnya lautan yang terbentang di hadapanmu. 
    Pikirannya melayang, memikirkan segala hal yang telah dilalui—dari hari-hari sibuk mengajar,hingga tantangan yang dihadapi dalam membangun bisnis kecilmu. 
    Semua seolah hilang begitu saja diiringi deburan ombak yang menenangkan.
    </font></p><br><br>
    <p align="center"><font face="arial"><font color="red">
    Tiba-tiba, sebuah ide muncul. Pantai ini bisa menjadi tempat yang sempurna untuk mengambil inspirasi dalam mengembangkan dashboard aplikasi pembelajaran yang sedang kamu kerjakan. Kamu bisa merancangnya 
    seperti alam—tenang namun dinamis, sederhana tapi penuh makna. Angin yang terus bertiup di pantai mengingatkanmu bahwa seperti dalam kehidupan, inovasi harus selalu bergerak, tak pernah berhenti.
    Saat matahari mulai terbenam, mewarnai langit dengan semburat jingga dan ungu, kamu tahu saatnya untuk kembali. Tapi, pengalaman sore itu di pantai meninggalkan jejak dalam pikiranmu, memberimu semangat baru. 
    Pantai telah mengajarimu banyak hal—tentang kedamaian, ketekunan, dan inspirasi yang bisa datang dari mana saja, 
    bahkan dari alam yang diam.Dalam perjalanan pulang, kamu tersenyum, yakin bahwa esok hari akan membawa keberhasilan baru—baik dalam mengajar, 
    bisnis es teh susu-mu, maupun proyek-proyek lain yang menantimu.
    </font></font></p>
